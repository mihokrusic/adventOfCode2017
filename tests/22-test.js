const assert = require('assert');
const dayNumber = '22';

const day = require('../solutions/' + dayNumber + '.js');

describe('Day ' + dayNumber, () => {

	var test = `
		..#
		#..
		...
	`;

	var real = `
		..####.###.##..##....##..
		.##..#.###.##.##.###.###.
		......#..#.#.....#.....#.
		##.###.#.###.##.#.#..###.
		#..##...#.....##.#..###.#
		.#..#...####...#.....###.
		##...######.#.###..#.##..
		###..#..##.###....##.....
		.#.#####.###.#..#.#.#..#.
		#.#.##.#.##..#.##..#....#
		..#.#.#.#.#.##...#.####..
		##.##..##...#..##..#.####
		#.#..####.##.....####.##.
		..####..#.#.#.#.##..###.#
		..#.#.#.###...#.##..###..
		#.####.##..###.#####.##..
		.###.##...#.#.#.##....#.#
		#...######...#####.###.#.
		#.####.#.#..#...##.###...
		####.#.....###..###..#.#.
		..#.##.####.#######.###..
		#.##.##.#.#.....#...#...#
		###.#.###..#.#...#...##..
		##..###.#..#####.#..##..#
		#......####.#.##.#.###.##
	`;

	describe('Part 1', () => {
		it('test', 		() => assert.equal(5587, day.part_one(test, 10000)));
		it('real', 		() => assert.equal(5176, day.part_one(real, 10000)));
	});

	describe('Part 2', () => {
		it('test - 100', 		() => assert.equal(26, day.part_two(test, 100)));
		it('test - 10000000', function(done) {
			this.timeout(360000);
			assert.equal(2511944, day.part_two(test, 10000000))
			done();
		});
		it('real - 10000000', function(done) {
			this.timeout(360000);
			assert.equal(2512017, day.part_two(real, 10000000))
			done();
		});
	});
});
